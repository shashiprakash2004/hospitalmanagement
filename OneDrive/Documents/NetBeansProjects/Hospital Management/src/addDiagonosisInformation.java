    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     String patientID = jTextField1.getText();

    if (patientID.isEmpty()) {
        JOptionPane.showMessageDialog(null, "PatientID Field is empty!");
        return; // Stop further execution
    }

    if (flag == 1) {
        String symptom = jTextField2.getText();
        String diagnosis = jTextField3.getText();
        String medicines = jTextField4.getText();
        String wardReq = jCheckBox1.isSelected() ? "Yes" : "No";
        String typeward = jCheckBox1.isSelected() ? (String) jComboBox1.getSelectedItem() : "";

        try {
            Connection con = ConnectionProvider.getCon();
            String sql = "INSERT INTO patientreport (patientID, symptom, diagnosis, medicines, wardreq, typeward) " +
                         "VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, patientID);
            pst.setString(2, symptom);
            pst.setString(3, diagnosis);
            pst.setString(4, medicines);
            pst.setString(5, wardReq);
            pst.setString(6, typeward);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new addDiagonosisInformation().setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();  // Print the full stack trace for debugging
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Please search for a patient first!");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

