    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            String patientID = jTextField1.getText();
    try {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement pst = con.prepareStatement("SELECT * FROM hospital WHERE patientID = ?");
        pst.setString(1, patientID);
        ResultSet rs = pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));

        if (!rs.next()) { // No record found
            jLabel2.setVisible(true);  // Display "PatientId Does not Exist" label
            flag = 1; // Ensure flag is not set if patientID is not found
        } else {
            jLabel2.setVisible(false);
            jTextField1.setVisible(false);  // Optionally hide the patientID input field
            flag = 1;  // Set the flag to allow the save operation
        }
    } catch (Exception e) {
        e.printStackTrace(); // Print the exception for debugging
        JOptionPane.showMessageDialog(null, "Connection Error");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

